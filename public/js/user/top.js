$(function(){
    $("div[id^='icon-']")
    .on('mouseover',function(){
        $(this).css({
            backgroundColor:'#f5deb3'
        });
    })
    .on('mouseout',function(){
        $(this).css({
            backgroundColor:''
        });
    });
});

